//Operadores de atribuição

// =  -> Atribuição
// += -> Adição
// -= -> Subtração
// /= -> Divisão
// *= -> Multiplicação
// %= -> resto

// ------====Atribuição====------

//Informa o valor a ser Atribuição a ação:
let Atribuição = 10
console.log(Atribuição)
console.log("-----------------------------------")

// ------====Adição====------
let Adição = 10 // modo de atribuição
Adição = Adição + 2 // modo normal
Adição +=2 // modo mais rapido com a mesma função---- Adiciona mais 2 a função 10 + 2

console.log(Adição)
console.log("-----------------------------------")

// ------====Subtração====------
let Subtração = 10 // modo de atribuição
Subtração = Subtração - 2 // modo normal
Subtração -=2 // modo mais rapido com a mesma função --- Retira 2 a função 10 - 2

console.log(Subtração)
console.log("-----------------------------------")

// ------====Divisão====------
let Divisão = 10 // modo de atribuição
Divisão = Divisão / 2 // modo normal
Divisão /=2 // modo mais rapido com a mesma função --- mostra o resto da função no caso de 10 / 2 nao a reste entao zero...

console.log(Divisão)
console.log("-----------------------------------")

// ------====Multiplicação====------
let Multiplicação = 10 // modo de atribuição
Multiplicação = Multiplicação * 2 // modo normal
Multiplicação *=2 // modo mais rapido com a mesma função --- mostra o resultado de 10 * 2

console.log(Multiplicação)
console.log("-----------------------------------")

